'''
С помощью параметра file можно перенаправить вывод в другое место - например, в файл. 
При этом, если параметр flush имеет значение False, выводимые значения будут принудительно записаны в файл. 
Перенаправление вывода мы подробно рассмотрим при изучении файлов.
'''
