list = ['one', 'two', 'three', 'four', '5ive',]
# slice definition is by the start:step:stop
###############################################
print()
print ('Full list - ',list[::])
print ('list[::] - ',list[1::])
print ('list[2::] - ', list[2::])
print ('list[3::] - ',list[3::])
print ('list[3::] - ',list[3::])

###############################################
print()
print ('Full list - ',list[::])
